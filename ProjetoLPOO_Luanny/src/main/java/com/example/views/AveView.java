    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int linhaSelecionada = tblAves.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma ave para editar!");
            return;
        }

        Long idAve = (Long) tblAves.getValueAt(linhaSelecionada, 0);

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("ProjetoLPOOE1_PU");
        EntityManager em = emf.createEntityManager();
        Ave aveSelecionada = em.find(Ave.class, idAve);

        if (aveSelecionada != null) {
            AveCadastro telaEdicao = new AveCadastro(aveSelecionada);
            telaEdicao.setVisible(true);
            telaEdicao.setLocationRelativeTo(this);
            telaEdicao.addWindowListener(new java.awt.event.WindowAdapter() {
                @Override
                public void windowClosed(java.awt.event.WindowEvent e) {
                    btnListarActionPerformed(evt);
                }
            });
        }

        em.close();
        emf.close();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       int linhaSelecionada = tblAves.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione uma ave para excluir.");
            return;
        }

        Long idAve = (Long) tblAves.getValueAt(linhaSelecionada, 0);
        int confirmacao = JOptionPane.showConfirmDialog(this, "Deseja excluir esta ave?", "Confirmação", JOptionPane.YES_NO_OPTION);

        if (confirmacao == JOptionPane.YES_OPTION) {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("ProjetoLPOOE1_PU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();

            Ave ave = em.find(Ave.class, idAve);
            if (ave != null) {
                em.remove(ave);
                JOptionPane.showMessageDialog(this, "Ave excluída com sucesso!");
            }

            em.getTransaction().commit();
            btnListarActionPerformed(evt);
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

