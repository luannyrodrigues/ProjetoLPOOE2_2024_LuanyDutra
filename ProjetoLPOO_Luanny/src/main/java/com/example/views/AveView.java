    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) tblAves.getModel();
        modelo.setRowCount(0);

        try {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("ProjetoLPOOE1_PU");
            EntityManager em = emf.createEntityManager();

            List<Ave> aves = em.createQuery("SELECT a FROM Ave a", Ave.class).getResultList();

            for (Ave ave : aves) {
                modelo.addRow(new Object[] {
                    ave.getId(),
                    ave.getNome(),
                    ave.getNomeCientifico(),
                    ave.getStatus(),
                    ave.getEnvergadura(),
                    ave.getTipoBico(),
                    ave.isPodeVoar(),
                    ave.isMigratoria()
                });
            }

            em.close();
            emf.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao listar aves: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnListarActionPerformed

